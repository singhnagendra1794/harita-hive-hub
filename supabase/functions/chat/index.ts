
import "https://deno.land/x/xhr@0.1.0/mod.ts";
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const openAIApiKey = Deno.env.get('OPENAI_API_KEY');

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

const systemPrompt = `You are Ava, a helpful GIS learning copilot for HaritaHive, an online GIS education platform. You are warm, expert, and patient.

Your expertise includes:
- QGIS software and tools
- Spatial analysis techniques
- GIS concepts and terminology
- Cartography and map making
- Remote sensing and satellite imagery
- Geospatial data formats (shapefiles, GeoTIFF, etc.)
- Python scripting for GIS
- Course materials and assignments

Guidelines:
- Be encouraging and supportive to students
- Provide step-by-step instructions when needed
- Use simple language but maintain technical accuracy
- Offer to provide examples or sample projects when relevant
- If you don't know something specific to their platform, suggest they contact human support
- Keep responses concise but thorough
- Always end with a follow-up question or offer to help further

Current context: You're helping students on a GIS learning platform with courses, tools, and assignments.`;

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { message, context } = await req.json();

    if (!message) {
      throw new Error('Message is required');
    }

    const response = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${openAIApiKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'gpt-4o-mini',
        messages: [
          { role: 'system', content: systemPrompt },
          { role: 'user', content: message }
        ],
        max_tokens: 500,
        temperature: 0.7,
      }),
    });

    if (!response.ok) {
      const error = await response.json();
      throw new Error(error.error?.message || 'Failed to get AI response');
    }

    const data = await response.json();
    const aiResponse = data.choices[0].message.content;

    return new Response(JSON.stringify({ response: aiResponse }), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });

  } catch (error) {
    console.error('Error in chat function:', error);
    return new Response(JSON.stringify({ error: error.message }), {
      status: 500,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  }
});
